// Embeds the built widget bundle into the api as a generated TS module so the
// worker can serve it at /widget.js without filesystem access at runtime.
// Runs as part of `pnpm --filter @llmchat/widget build` (after vite build).
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const source = resolve(here, "../dist/widget.js");
const target = resolve(
	here,
	"../../../apps/api/src/generated/widget-bundle.ts",
);

const bundle = await readFile(source, "utf8");
const banner =
	"// Generated by `pnpm --filter @llmchat/widget build`. Do not edit.\n// prettier-ignore\n";
await mkdir(dirname(target), { recursive: true });
await writeFile(
	target,
	`${banner}export const widgetBundle: string = ${JSON.stringify(bundle)};\n`,
);
console.log(
	`[widget] embedded ${(bundle.length / 1024).toFixed(0)}KB bundle into apps/api/src/generated/widget-bundle.ts`,
);
